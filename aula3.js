// BOOLEANOS: "true" e "false"
/* Operadores de comparação:
== -> igualdade
!= -> desigualdade
>  -> maior que
<  -> menor que
>= -> maior ou igual que
<= -> menor ou igual que
* Operador de igualdade estrita *
=== -> igualdade estrita (compara conteúdo e tipo da variável)
!== -> desigualdade estrita
*/
console.log(10 == "10")
console.log(true == 1)
console.log(false == 0)
// Igualdade estrita
console.log(1 === "1")
console.log(true === 1)
console.log(false === 0)
